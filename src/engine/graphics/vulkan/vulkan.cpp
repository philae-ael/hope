#define FLAG_STR_HELPER(sb, flags, x, n) \
  core::string_node n;                   \
  if ((flags & x) != 0) {                \
    sb.push_str8(&n, STRINGIFY(x) ""_s); \
  }

#define FLAG_STR(sb, flags, x) FLAG_STR_HELPER(sb, flags, x, CONCAT(n_, __COUNTER__))

